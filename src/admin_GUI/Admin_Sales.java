    private void tableDB(){
        dbConnectionRepo db = new dbConnectionRepo();
        List<salesTable> list = db.sales_GetRecordsDAO();
        DefaultTableModel d = (DefaultTableModel) tblTransaction.getModel();
        d.setRowCount(0);
        Object rowData[] = new Object[4];
        for(int i = 0; i < list.size(); i++){
           rowData[0] = list.get(i).getid();
           rowData[1] = list.get(i).getdate();
           rowData[2] = list.get(i).getsubtotal();
           rowData[3] = list.get(i).getstatus();
           d.addRow(rowData);
        }
    }
    
